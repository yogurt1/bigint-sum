import { sum } from '../src';

test('sum two numbers', () => {
  const result = sum(
    '12383838491910020293939391010102929393838382092',
    '342345352332222243434635345482829383545454553242565656334343434242424'
  );

  expect(result).toBe(
    '342345352332222243434647729321321293565748492633575759263737272624516'
  );
});
